declare(strict_types=1);

namespace pocketmine\network\mcpe\protocol\types\cereal;

use pmmp\encoding\ByteBufferReader;
use pmmp\encoding\ByteBufferWriter;
use pmmp\encoding\LE;
use pocketmine\network\mcpe\protocol\types\GetTypeIdFromConstTrait;

final class DynamicValueLong extends DynamicValue{
	use GetTypeIdFromConstTrait;

	public const ID = DynamicValueType::LONG;

	public function __construct(
		private int $value
	){}

	protected static function readValue(ByteBufferReader $in) : self{
		return new self(LE::readSignedLong($in));
	}

	protected function writeValue(ByteBufferWriter $out) : void{
		LE::writeSignedLong($out, $this->value);
	}
}